import UIKit

// MARK: - Colors
extension UIColor {

    static var icrGray: UIColor {
        get {
            return UIColor(named: "grayButton")!
        }
    }

    static var icrOrange: UIColor {
        get {
            return UIColor(named: "orangeButton")!
        }
    }

    static var icrBlack: UIColor {
        get {
            return  UIColor(named: "blackButton")!
        }
    }
}
